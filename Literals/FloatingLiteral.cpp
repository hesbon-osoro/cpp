#include<iostream>
using namespace std;
int main()
{
float fl;
fl=314159E-5L ;//5 decimals behind
cout<<"no in floating literal form: 314159E-5L = "<<fl<<endl;
fl=6.02e23; // 6.02 x 10^2
cout<<"no in floating literal form: 6.02e23 = "<<fl<<endl;
fl=1.6e-19;    // 1.6 x 10^-19
cout<<"no in floating literal form: 1.6e-19 = "<<fl<<endl;
return 0;
}
